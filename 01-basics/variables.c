#include<stdio.h>
int main(){
  int a;        // Variable Declaration
  a = 10;       // Variable Initialization
  printf("%d\n",a);
  float b = 20.08;
  printf("%f\n",b);
  char c = 's';
  printf("%c\n",c);
  double d = 20.12345;
  printf("%lf\n",d);
  
  return 0;
}

// %d → integer
// %f → float
// %c → character
// %lf → double
